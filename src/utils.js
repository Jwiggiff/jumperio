let mySeed;
let rng;

export function seedRandom(seed = mySeed) {
  mySeed = seed;
  rng = seedrandom(seed);
}

export function random(min, max) {
  return rng() * (max - min) + min;
}

export function constrain(n, min, max) {
  if (n < min) return min;
  else if (n > max) return max;
  else return n;
}

export function generatePlatform(lastPlatform, y = 0) {
  let w = random(30, 300);
  let x = random(0, 1000 - 300);
  y = lastPlatform.y + 100;

  if (x + w < lastPlatform.left) {
    x = constrain(x, lastPlatform.left - w - 100, 1000);
  } else if (x > lastPlatform.right) {
    x = constrain(x, 0, lastPlatform.right + 100);
  }
  return new Platform(x, y, w);
}

export function keyIsDown(key) {
  return window.key == key;
}

export function convertCoords(x, y) {
  return {
    x: (x / 1000) * canvas.width,
    y: canvas.height - y + vph,
  };
}

export function convertWidth(w) {
  return (w / 1000) * canvas.width;
}

export function randomColor() {
  return colors[Math.floor(Math.random() * colors.length)];
}
